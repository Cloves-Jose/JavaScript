//Valores literais
/**
 * Valores literais são os 
 * valores que não estão 
 * associados a nenhuma 
 * variável
 */
console.log(10);

//Variáveis
/**
 * Variáveis são compartimentos
 * na memória criados para possibilitar
 * a reutilização de valores associados
 * a essas variáveis
 */
var variavel = 10;
console.log(variavel);

//Tipos de variáveis

/**
 * var
 * "var" é a forma antiga de se criar
 * variáveis em JavaScript, já não tem
 * mais usabilidade
 * 
 * let
 * "let" é a forma atual de se criar
 * variáveis no JavaScript. Ela é uma
 * variável que permite alteração
 * durante a execução do programa (JIT).
 * 
 * const
 * "const" é outra maneira de se criar
 * variáveis no JavScript. No entanto
 * variáveis "const" não podem ser alteradas
 * durante a execução do código.
 */