//Sentença de código
/**
 * Sentença é um comando
 * "simples" de uma única linha
 * cada sentença tem o objetivo de 
 * passar apenas um comando para
 * o computador
 */
console.log("Legal 2");

//Bloco de código
/**
 * O bloco é delimitado pela
 * abertura e fechamento das 
 * chaves. O comando for é uma
 * sentença que executa um bloco
 * de código.
 */
for(var i = 0; i <= 10; i++){
    console.log("Legal 1");
}

/**
 * É importante lembrar que em um software,
 * na maioria das vezes, a sequência de código 
 * importa. Portanto devemos sempre pensar 
 * na sequência de execução.
 * 
 */